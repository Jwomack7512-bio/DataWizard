output$plotlyPlot <- renderPlotly(
  if(input$plotly_plotType == "Scatter"){
    ggplotly(plotScatterInput())
  }
  else if(input$plotly_plotType == "Histogram"){
    ggplotly(plotHistogramInput())
  }
  else if(input$plotly_plotType == "Boxplot"){
    ggplotly(plotBoxplotInput())
  }
  else if(input$plotly_plotType == "Violin"){
    ggplotly(plotViolinInput())
  }
  else if(input$plotly_plotType == "Line"){
    ggplotly(plotLineplotInput())
  }
  else if(input$plotly_plotType == "Bar"){
    ggplotly(plotBarplotInput())
  }
  
  
)
